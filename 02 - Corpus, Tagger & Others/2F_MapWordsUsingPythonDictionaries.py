# Creating a dictionary with car properties
thisdict = {
    "brand": "Ford",
    "model": "Mustang",
    "year": 1964
}

# Printing the entire dictionary
print(thisdict)

# Printing the value associated with the key "brand"
print(thisdict["brand"])

# Printing the number of key-value pairs in the dictionary
print(len(thisdict))

# Printing the type of the variable 'thisdict'
print(type(thisdict))
